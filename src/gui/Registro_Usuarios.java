    private void jPass_Usuario_Re_CoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jPass_Usuario_Re_CoKeyReleased
        char[]temp;
        temp = jPass_Usuario_Re_Co.getPassword();
        
        if(Arrays.compare(temp,password_temp)==0){
            password_ok = true;
            jPass_Usuario_Re_Co.setBorder(new JPasswordField().getBorder());
        }else{
            password_ok = false;
            jPass_Usuario_Re_Co.setBorder(javax.swing.BorderFactory.createLineBorder(Color.RED));
        }
    }//GEN-LAST:event_jPass_Usuario_Re_CoKeyReleased


