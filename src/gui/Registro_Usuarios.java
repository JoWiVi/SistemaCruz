    private void jBtn_RegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtn_RegistrarActionPerformed
        String nombre_usuario, contraseña_usuario, rol_empleado;
        nombre_usuario = jTxt_Usuario_R.getText();
        rol_empleado = jTxt_Rol_Usuario.getText();

        if (password_temp != null) {
            contraseña_usuario = new String(password_temp);

            if (!nombre_usuario.equals("") && !rol_empleado.equals("") && password_ok) {
                // Intentar insertar el usuario
                if (cr.insertar(nombre_usuario, contraseña_usuario, rol_empleado)) {
                    limpiarcampos();
                    JOptionPane.showMessageDialog(rootPane, "Se registró usuario");
                } else {
                    limpiarcampos();
                    JOptionPane.showMessageDialog(rootPane, "El nombre de usuario ya existe");
                }
            } else {
                limpiarcampos();
                JOptionPane.showMessageDialog(rootPane, "Tiene que ingresar el usuario y contraseña");
            }
        } else {
            limpiarcampos();
            JOptionPane.showMessageDialog(rootPane, "Contraseñas no coiciden");
        }
    }//GEN-LAST:event_jBtn_RegistrarActionPerformed

